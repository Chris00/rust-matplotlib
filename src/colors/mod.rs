//! Matplotlib colors.
//!
//! https://matplotlib.org/stable/gallery/color/named_colors.html

// Unlike Matplotlib colors, we want the colors to be statically
// checked as much as possible to avoid a plot to stop the whole
// program because a color is incorrect.

// https://matplotlib.org/stable/users/explain/colors/colors.html

/// Trait that color representations must satisfy.
pub trait Color {
    /// Return the RGBA components of the color as numbers in
    /// \[0., 1.\].
    fn rgba(&self) -> [f64; 4];
}

impl Color for [f64; 3] {
    fn rgba(&self) -> [f64; 4] {
        [self[0].clamp(0., 1.), self[1].clamp(0., 1.),
            self[2].clamp(0., 1.), 1.]
    }
}

impl Color for [f64; 4] {
    fn rgba(&self) -> [f64; 4] {
        [self[0].clamp(0., 1.), self[1].clamp(0., 1.),
            self[2].clamp(0., 1.), self[3].clamp(0., 1.)]
    }
}

/// For any color `c`, one can change its alpha value by using
/// `(c, alpha)` where `alpha` will be clamped to \[0., 1.\].
impl<C: Color> Color for (C, f64) {
    fn rgba(&self) -> [f64; 4] {
        let mut c = self.0.rgba();
        c[3] = self.1.clamp(0., 1.);
        c
    }
}

impl Color for f64 {
    /// Return the gray color with intensity `self`.  If `self` ∉
    /// \[0., 1.\], it is clamped to that interval.  NaN is treated as
    /// a fully transparent color.
    fn rgba(&self) -> [f64; 4] {
        if self.is_nan() {
            [0., 0., 0., 0.]
        } else {
            let f = self.clamp(0., 1.);
            [f, f, f, 1.]
        }
    }
}

/// [Base colors](https://matplotlib.org/stable/gallery/color/named_colors.html#base-colors).
///
/// Remark: The colors green, cyan, magenta, and yellow do not
/// coincide with X11/CSS4 colors.  Their particular shades were chosen
/// for better visibility of colored lines against typical backgrounds.
#[derive(Clone, Copy)]
pub enum Base {
    /// Blue
    B,
    /// Green
    G,
    /// Red
    R,
    /// Cyan
    C,
    /// Magenta
    M,
    /// Yellow
    Y,
    /// Black
    K,
    /// White
    W,
}

impl Color for Base {
    fn rgba(&self) -> [f64; 4] {
        match self {
            Base::B => [0., 0., 1., 1.],
            Base::G => [0., 0.5, 0., 1.],
            Base::R => [1., 0., 0., 1.],
            Base::C => [0., 0.75, 0.75, 1.],
            Base::M => [0.75, 0., 0.75, 1.],
            Base::Y => [0.75, 0.75, 0., 1.],
            Base::K => [0., 0., 0., 1.],
            Base::W => [1., 1., 1., 1.],
        }
    }
}

